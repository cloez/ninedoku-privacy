/// 지원 언어
enum AppLanguage {
  korean('한국어', Locale('ko')),
  english('English', Locale('en')),
  japanese('日本語', Locale('ja')),
  chinese('中文', Locale('zh'));

  const AppLanguage(this.label, this.locale);
  final String label;
  final Locale locale;

  static AppLanguage fromCode(String? code) {
    switch (code) {
      case 'en':
        return english;
      case 'ja':
        return japanese;
      case 'zh':
        return chinese;
      default:
        return korean;
    }
  }
}

